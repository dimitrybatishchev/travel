            <p>Гонконг расположен в тропике Рака и имеет муссонный субтропический климат, характеризующийся жарким и очень влажным летом, иногда с возможностью тропических циклонов, а зима мягкая, солнечная и сухая. Около 80% дождей с мая по сентябрь. Температура претерпевает не сильные колебания в течение всего года, зимой минимальная средняя температура 14 ° C, а максимальная - 18 ° C, а летом, соответственно, 27 ° C и 31 ° C.</p>
